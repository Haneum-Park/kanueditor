function render() {
	// ? editor wrapper
	const editor = document.createElement("div");
	editor.classList.add("kanu-editor");

	// ? 툴바의 모든 버튼을 삽입 후 삽입
	editor.appendChild(toolbarRender());
	editor.appendChild(contentRender());
	editor.appendChild(resizerRender());

	// ? rendering document
	document.body.appendChild(editor);
}

// ? init 함수 렌더링과 동시에 실행
function init() {
	render();
}

